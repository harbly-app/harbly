//! Rust-side localization: native menu bar labels + operation-description
//! templates for the undo log. The language is persisted in config.json, and
//! set_language rebuilds the menu. An undo entry's label is generated in the
//! language active at the time of the operation (after a language switch, old
//! entries keep their original text — acceptable behavior, consistent with Finder).

pub const SUPPORTED: [&str; 6] = ["zh-CN", "zh-TW", "en", "ja", "ko", "es"];

pub fn normalize(lang: &str) -> &'static str {
    SUPPORTED
        .iter()
        .find(|l| **l == lang)
        .copied()
        .unwrap_or("zh-CN")
}

