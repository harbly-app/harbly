pub fn l(lang: &str) -> &'static L {
    match normalize(lang) {
        "zh-TW" => &ZH_TW,
        "en" => &EN,
        "ja" => &JA,
        "ko" => &KO,
        "es" => &ES,
        _ => &ZH_CN,
    }
}
