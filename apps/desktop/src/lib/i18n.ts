export type TFn = (
  key: string,
  params?: Record<string, string | number>,
) => string;

export function makeT(lang: Lang): TFn {
  const d = DICTS[lang];
  return (key, params) => {
    let s: string | undefined;
    const n = params?.n;
    if (typeof n === "number" && n === 1) s = d[`${key}One`];
    s = s ?? d[key] ?? zhCN[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.split(`{${k}}`).join(String(v));
      }
    }
    return s;
  };
}

